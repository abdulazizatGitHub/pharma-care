-- 15. RLS POLICIES
--
-- Access model:
--   superadmin — full read + write on all ledger tables
--   admin      — read-only on all ledger tables
--   pharmacist — no access (ledger is a financial module, not clinical)
--
-- Immutability at RLS level (in addition to triggers):
--   journal_lines         — no UPDATE policy, no DELETE policy
--   borrowing_transactions — no UPDATE policy
--   supplier_payments     — no UPDATE policy, no DELETE policy
--   customer_payments     — no UPDATE policy, no DELETE policy
--
-- journal_entries UPDATE is allowed for superadmin to support:
--   draft → posted promotion and reversal metadata writes (reversed_by, status).
--   The prevent_posted_entry_mutation trigger enforces field-level immutability
--   inside those allowed UPDATE operations.
-- ===========================================================================

