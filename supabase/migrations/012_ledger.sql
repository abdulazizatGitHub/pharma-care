-- PharmaCare — Migration 012: Ledger & Accounting Module (Phase 7A)
-- File: supabase/migrations/012_ledger.sql
-- Spec: PHARMACARE_PHASE_7_LEDGER.md
--
-- Creates (in dependency order):
--   1.  accounts              — chart of accounts
--   2.  journal_entries       — immutable financial journal header
--   3.  journal_lines         — individual debit/credit lines (fully immutable)
--   4.  Immutability triggers — blocks UPDATE/DELETE on lines; blocks mutation
--                               of core financial fields on posted entries
--   5.  verify_journal_balance()   — check function (returns BOOLEAN)
--   6.  check_entry_balance()      — placeholder trigger (real check in RPC)
--   7.  borrowing_pharmacies  — neighboring pharmacy credit tracking
--   8.  borrowing_transactions — medicine + cash transactions with borrowing pharmacies
--   9.  supplier_payments     — permanent payment records for supplier balances
--   10. customer_payments     — permanent payment records for customer udhaar
--   11. exchange_rates        — currency rate history (unique per currency+date)
--   12. post_journal_entry()  — atomic RPC: generates entry_no, inserts header +
--                               lines, and enforces double-entry balance
--   13. Chart of accounts seed (23 pre-seeded accounts, is_system on core ones)
--   14. Settings seed: base_currency, multicurrency_enabled, exchange_rate_api_key
--   15. RLS policies on all new tables
--
-- Immutability guarantees (enforced at DB layer, not app layer):
--   • journal_lines: BEFORE UPDATE OR DELETE trigger raises EXCEPTION unconditionally.
--     RLS also has no UPDATE/DELETE policy — two independent guards.
--   • journal_entries (posted/reversed): BEFORE UPDATE trigger blocks changes to
--     entry_date, description, reference_*, currency, exchange_rate.
--     Allows only reversal-metadata updates (status, reversed_by, reversal_of).
--
-- User-requested constraint (not in spec):
--   CONSTRAINT no_future_dates on journal_entries prevents entry_date from being
--   more than 1 day ahead of CURRENT_DATE (evaluated at INSERT/UPDATE time).
--   The +1 day tolerance accommodates legitimate timezone edge-cases.
--
-- COGS source: stock_batches.purchase_price (NUMERIC(10,2), nullable) — confirmed
--   present in 001/008. Phase 7B will join this column when updating complete_sale().
-- =============================================================================


-- ===========================================================================
-- 1. ACCOUNTS (chart of accounts)
-- is_system = TRUE: core accounts used in auto-posting — must not be deactivated.
-- normal_balance: asset/cogs/expense = debit; liability/equity/revenue = credit.
-- parent_code self-references accounts(code) for optional hierarchy display.
-- ===========================================================================

CREATE TABLE accounts (
  id             UUID        DEFAULT gen_random_uuid() PRIMARY KEY,
  code           TEXT        NOT NULL UNIQUE,
  name           TEXT        NOT NULL,
  account_type   TEXT        NOT NULL CHECK (
                   account_type IN (
                     'asset', 'liability', 'equity', 'revenue', 'cogs', 'expense'
                   )
                 ),
  normal_balance TEXT        NOT NULL CHECK (normal_balance IN ('debit', 'credit')),
  parent_code    TEXT        REFERENCES accounts(code),
  is_system      BOOLEAN     DEFAULT FALSE,
  is_active      BOOLEAN     DEFAULT TRUE,
  description    TEXT,
  currency       TEXT        DEFAULT 'PKR',
  created_at     TIMESTAMPTZ DEFAULT NOW() NOT NULL,
  updated_at     TIMESTAMPTZ DEFAULT NOW() NOT NULL,
  created_by     UUID        REFERENCES profiles(id),
  is_deleted     BOOLEAN     DEFAULT FALSE,
  deleted_at     TIMESTAMPTZ
);

CREATE TRIGGER set_accounts_updated_at
  BEFORE UPDATE ON accounts
  FOR EACH ROW EXECUTE FUNCTION set_updated_at();


-- ===========================================================================
-- 2. JOURNAL_ENTRIES (immutable financial ledger headers)
--
-- entry_no format: JE-YYYYMMDD-XXXX (generated by post_journal_entry RPC)
-- status flow: draft → posted → reversed
--   draft   : editable, not yet committed
--   posted  : financially committed, core fields locked by trigger
--   reversed: original entry for which a reversal was created
--
-- reversed_by / reversal_of: cross-link between an entry and its reversal.
-- NO updated_at — the header is written once and only reversal metadata may change.
-- NO is_deleted — journal entries cannot be deleted, ever.
--
-- no_future_dates constraint (user requirement): entry_date may not exceed
-- CURRENT_DATE + 1 day. Evaluated at INSERT and UPDATE time.
-- ===========================================================================

CREATE TABLE journal_entries (
  id             UUID        DEFAULT gen_random_uuid() PRIMARY KEY,
  entry_no       TEXT        NOT NULL UNIQUE,
  entry_date     DATE        NOT NULL DEFAULT CURRENT_DATE,
  description    TEXT        NOT NULL,

  reference_type TEXT        CHECK (reference_type IN (
                   'sale', 'sale_return', 'purchase_order', 'grn',
                   'supplier_payment', 'customer_payment',
                   'borrowing_out', 'borrowing_in',
                   'borrowing_payment', 'expense',
                   'manual', 'opening_balance', 'adjustment'
                 )),
  reference_id   UUID,

  status         TEXT        NOT NULL DEFAULT 'posted' CHECK (
                   status IN ('draft', 'posted', 'reversed')
                 ),
  reversed_by    UUID        REFERENCES journal_entries(id),
  reversal_of    UUID        REFERENCES journal_entries(id),

  currency       TEXT        NOT NULL DEFAULT 'PKR',
  exchange_rate  NUMERIC(15,6) DEFAULT 1.000000,

  created_at     TIMESTAMPTZ DEFAULT NOW() NOT NULL,
  created_by     UUID        REFERENCES profiles(id),

  CONSTRAINT no_self_reversal CHECK (reversed_by IS DISTINCT FROM id),
  CONSTRAINT no_future_dates  CHECK (
    entry_date <= CURRENT_DATE + INTERVAL '1 day'
  )
);

CREATE INDEX idx_journal_entries_date      ON journal_entries(entry_date);
CREATE INDEX idx_journal_entries_reference ON journal_entries(reference_type, reference_id);
CREATE INDEX idx_journal_entries_status    ON journal_entries(status);


-- ===========================================================================
-- 3. JOURNAL_LINES (individual debit/credit lines — FULLY IMMUTABLE)
--
-- amount     : in the transaction currency (same as journal_entries.currency)
-- amount_pkr : pre-calculated PKR equivalent (amount × exchange_rate)
--              Stored explicitly to avoid repeated floating-point recalculation
--              and to keep PKR-denominated reports fast.
-- party_type / party_id: optional link to a specific counterparty for
--   generating party-level ledger views without joining back to journal_entries.
--
-- NO updated_at, NO is_deleted — lines are immutable by design and trigger.
-- ===========================================================================

CREATE TABLE journal_lines (
  id          UUID          DEFAULT gen_random_uuid() PRIMARY KEY,
  entry_id    UUID          NOT NULL REFERENCES journal_entries(id),
  account_id  UUID          NOT NULL REFERENCES accounts(id),
  amount      NUMERIC(15,4) NOT NULL CHECK (amount > 0),
  direction   TEXT          NOT NULL CHECK (direction IN ('debit', 'credit')),
  amount_pkr  NUMERIC(15,4) NOT NULL CHECK (amount_pkr > 0),
  party_type  TEXT          CHECK (party_type IN ('supplier', 'customer', 'pharmacy')),
  party_id    UUID,
  description TEXT,
  created_at  TIMESTAMPTZ   DEFAULT NOW() NOT NULL
);

CREATE INDEX idx_journal_lines_entry   ON journal_lines(entry_id);
CREATE INDEX idx_journal_lines_account ON journal_lines(account_id);
CREATE INDEX idx_journal_lines_party   ON journal_lines(party_type, party_id);


-- ===========================================================================
-- 4. IMMUTABILITY TRIGGERS
--
-- 4a. journal_lines: block ALL UPDATE and DELETE.
--     The SECURITY DEFINER post_journal_entry() only ever INSERTs lines, so
--     this trigger protects against any direct table mutation.
--
-- 4b. journal_entries: block mutation of financial fields once the entry
--     reaches 'posted' or 'reversed' status.
--     Allowed on posted entries: status → 'reversed', setting reversed_by /
--     reversal_of for the reversal workflow. Disallowed: entry_date, description,
--     reference_*, currency, exchange_rate.
-- ===========================================================================

CREATE OR REPLACE FUNCTION prevent_journal_line_mutation()
RETURNS TRIGGER AS $$
BEGIN
  RAISE EXCEPTION
    'journal_lines are immutable — UPDATE and DELETE are not permitted (entry_id: %)',
    OLD.entry_id;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER journal_lines_immutable
  BEFORE UPDATE OR DELETE ON journal_lines
  FOR EACH ROW EXECUTE FUNCTION prevent_journal_line_mutation();


CREATE OR REPLACE FUNCTION prevent_posted_entry_mutation()
RETURNS TRIGGER AS $$
BEGIN
  IF OLD.status IN ('posted', 'reversed') THEN
    IF (
      NEW.entry_date     IS DISTINCT FROM OLD.entry_date     OR
      NEW.description    IS DISTINCT FROM OLD.description    OR
      NEW.reference_type IS DISTINCT FROM OLD.reference_type OR
      NEW.reference_id   IS DISTINCT FROM OLD.reference_id   OR
      NEW.currency       IS DISTINCT FROM OLD.currency       OR
      NEW.exchange_rate  IS DISTINCT FROM OLD.exchange_rate
    ) THEN
      RAISE EXCEPTION
        'Cannot modify financial fields of a % journal entry (id: %)',
        OLD.status, OLD.id;
    END IF;
  END IF;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER journal_entries_protect_posted
  BEFORE UPDATE ON journal_entries
  FOR EACH ROW EXECUTE FUNCTION prevent_posted_entry_mutation();


-- ===========================================================================
-- 5. verify_journal_balance(p_entry_id UUID) → BOOLEAN
--
-- Returns TRUE if the entry balances (|debits − credits| < 0.0001 PKR).
-- Called by server actions for pre-flight checks on manual entries.
-- The tolerance of 0.0001 accommodates floating-point rounding during
-- multi-currency conversions while being too small to mask real errors.
-- ===========================================================================

CREATE OR REPLACE FUNCTION verify_journal_balance(p_entry_id UUID)
RETURNS BOOLEAN AS $$
DECLARE
  v_debit_total  NUMERIC(15,4);
  v_credit_total NUMERIC(15,4);
BEGIN
  SELECT
    SUM(CASE WHEN direction = 'debit'  THEN amount_pkr ELSE 0 END),
    SUM(CASE WHEN direction = 'credit' THEN amount_pkr ELSE 0 END)
  INTO v_debit_total, v_credit_total
  FROM journal_lines
  WHERE entry_id = p_entry_id;

  RETURN ABS(
    COALESCE(v_debit_total,  0) -
    COALESCE(v_credit_total, 0)
  ) < 0.0001;
END;
$$ LANGUAGE plpgsql STABLE;

REVOKE ALL    ON FUNCTION verify_journal_balance(UUID) FROM PUBLIC;
GRANT EXECUTE ON FUNCTION verify_journal_balance(UUID) TO authenticated;


