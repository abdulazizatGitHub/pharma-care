-- ===========================================================================
-- 12. post_journal_entry() — atomic journal entry RPC
--
-- All journal entry creation (auto-posted from sales/GRN and manual entries)
-- goes through this single function.
--
-- Algorithm:
--   1. Generate entry_no (JE-YYYYMMDD-XXXX) — not transaction-safe for high
--      concurrency, acceptable for single-branch pharmacy (same caveat as POs).
--   2. INSERT journal_entries header with status='posted'.
--   3. For each line in p_lines JSONB array:
--        a. Resolve account_id from account_code.
--        b. Calculate amount_pkr = amount × exchange_rate.
--        c. INSERT journal_lines row.
--        d. Accumulate running debit/credit sums.
--   4. RAISE EXCEPTION if |debits − credits| ≥ 0.0001.
--
-- p_lines JSONB element schema:
--   {
--     "account_code": "1000",      -- required; must exist in accounts table
--     "direction":    "debit",     -- required; 'debit' | 'credit'
--     "amount":       "100.00",    -- required; positive numeric string
--     "party_type":   "customer",  -- optional; 'supplier'|'customer'|'pharmacy'
--     "party_id":     "<uuid>",    -- optional; UUID string
--     "description":  "line note"  -- optional
--   }
--
-- Returns: UUID of the created journal_entries row.
-- Raises:  'Account not found or inactive: <code>'
--          'Journal entry does not balance. Debits: X, Credits: Y'
--
-- SECURITY DEFINER: bypasses RLS so auto-posting from complete_sale() and
-- complete_grn() works regardless of the caller's role context.
-- ===========================================================================

CREATE OR REPLACE FUNCTION post_journal_entry(
  p_entry_date     DATE,
  p_description    TEXT,
  p_reference_type TEXT,
  p_reference_id   UUID,
  p_currency       TEXT,
  p_exchange_rate  NUMERIC,
  p_lines          JSONB,
  p_created_by     UUID
)
RETURNS UUID AS $$
DECLARE
  v_entry_id   UUID;
  v_entry_no   TEXT;
  v_line       JSONB;
  v_account_id UUID;
  v_debit_sum  NUMERIC(15,4) := 0;
  v_credit_sum NUMERIC(15,4) := 0;
  v_amount_pkr NUMERIC(15,4);
BEGIN
  -- Generate entry number: JE-YYYYMMDD-XXXX (daily sequence reset)
  SELECT 'JE-' || TO_CHAR(p_entry_date, 'YYYYMMDD') || '-' ||
    LPAD(
      (SELECT COUNT(*) + 1
       FROM journal_entries
       WHERE entry_no LIKE 'JE-' || TO_CHAR(p_entry_date, 'YYYYMMDD') || '-%'
      )::TEXT,
      4, '0'
    )
  INTO v_entry_no;

  -- Insert journal entry header
  INSERT INTO journal_entries (
    entry_no, entry_date, description,
    reference_type, reference_id,
    currency, exchange_rate,
    status, created_by
  ) VALUES (
    v_entry_no, p_entry_date, p_description,
    p_reference_type, p_reference_id,
    p_currency, p_exchange_rate,
    'posted', p_created_by
  ) RETURNING id INTO v_entry_id;

  -- Insert lines and accumulate balance totals
  FOR v_line IN SELECT * FROM jsonb_array_elements(p_lines) LOOP

    SELECT id INTO v_account_id
    FROM accounts
    WHERE code      = v_line->>'account_code'
      AND is_active = TRUE
      AND is_deleted = FALSE;

    IF v_account_id IS NULL THEN
      RAISE EXCEPTION 'Account not found or inactive: %', v_line->>'account_code';
    END IF;

    v_amount_pkr := (v_line->>'amount')::NUMERIC * p_exchange_rate;

    INSERT INTO journal_lines (
      entry_id, account_id,
      amount, direction, amount_pkr,
      party_type, party_id, description
    ) VALUES (
      v_entry_id,
      v_account_id,
      (v_line->>'amount')::NUMERIC,
      v_line->>'direction',
      v_amount_pkr,
      v_line->>'party_type',
      CASE WHEN (v_line->>'party_id') IS NOT NULL
           THEN (v_line->>'party_id')::UUID
           ELSE NULL
      END,
      v_line->>'description'
    );

    IF v_line->>'direction' = 'debit' THEN
      v_debit_sum  := v_debit_sum  + v_amount_pkr;
    ELSE
      v_credit_sum := v_credit_sum + v_amount_pkr;
    END IF;

  END LOOP;

  -- CRITICAL: Enforce double-entry balance before the transaction commits
  IF ABS(v_debit_sum - v_credit_sum) >= 0.0001 THEN
    RAISE EXCEPTION
      'Journal entry does not balance. Debits: %, Credits: %',
      v_debit_sum, v_credit_sum;
  END IF;

  RETURN v_entry_id;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

REVOKE ALL    ON FUNCTION post_journal_entry(DATE,TEXT,TEXT,UUID,TEXT,NUMERIC,JSONB,UUID) FROM PUBLIC;
GRANT EXECUTE ON FUNCTION post_journal_entry(DATE,TEXT,TEXT,UUID,TEXT,NUMERIC,JSONB,UUID) TO authenticated;


